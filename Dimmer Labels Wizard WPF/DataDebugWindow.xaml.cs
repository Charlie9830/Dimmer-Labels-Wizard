namespace Dimmer_Labels_Wizard_WPF
{
    /// <summary>
    /// Interaction logic for DataDebugWindow.xaml
    /// </summary>
    public partial class DataDebugWindow : Window, INotifyPropertyChanged
    {
        public DataDebugWindow()
        {
            InitializeComponent();
        }


        protected ObservableCollection<DimmerDistroUnit> _Units = new ObservableCollection<DimmerDistroUnit>();

        public ObservableCollection<DimmerDistroUnit> Units
        {
            get { return _Units; }
            set
            {
                if (_Units != value)
                {
                    _Units = value;

                    // Notify.
                    RaisePropertyChanged(nameof(Units));
                }
            }
        }

        #region Interfaces.
        public event PropertyChangedEventHandler PropertyChanged;

        protected void RaisePropertyChanged(string propertyName)
        {
            if (PropertyChanged != null)
            {
                PropertyChanged(this, new PropertyChangedEventArgs(propertyName));
            }
        }
        #endregion
    }

}
